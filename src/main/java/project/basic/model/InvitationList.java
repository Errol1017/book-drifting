public class InvitationList {

    private String id;
    private String name;
    private String mobile;
    private String agency;

    public InvitationList() {
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getMobile() {
        return mobile;
    }

    public String getAgency() {
        return agency;
    }
}
